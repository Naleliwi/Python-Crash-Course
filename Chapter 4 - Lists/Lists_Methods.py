# Index Methods:


Names = ['Saleh', 'Khalid', 'Noura', 'Nada', 'Mohammed']


# finding index for Khalid

Names.index('Khalid')


# Adding Noof at the end of the list

Names.append('Noof')


# Inserting Sara between Mohammed and Noof

Names.insert(5, 'Sara')

# Removing Saleh

Names.remove('Saleh')

# Deleting name that has index number 3

del Names[3]


Ages = [23 , 33, 40, 36, 27]

# Sorting numeric values accendingly

Ages.sort()


# Sorting Names Alphabitically

Names.sort()

# The opposite

Names.sort(reverse =True)
